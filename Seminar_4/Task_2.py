# Напишите функцию принимающую на вход только ключевые параметры и возвращающую словарь, где ключ — значение переданного аргумента, а значение — имя аргумента. Если ключ не хешируем, используйте его строковое представление.

def reverse_kwargs(**kwargs):
    """
    Принимает только ключевые параметры и возвращает словарь, 
    где ключ — значение аргумента, а значение — имя аргумента.
    
    Если значение не хэшируемо, используется его строковое представление.
    """
    result = {}
    for key, value in kwargs.items():
        try:
            # Проверяем, можно ли использовать значение как ключ
            hash(value)  # Если хэшируемо, не возникнет исключения
            result[value] = key
        except TypeError:
            # Если значение не хэшируемо, используем его строковое представление
            result[str(value)] = key
    return result

# Пример использования
output = reverse_kwargs(a=42, b="hello", c=[1, 2, 3], d={"key": "value"})
print(output)
