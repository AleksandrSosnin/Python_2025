# Дан список повторяющихся элементов. Вернуть список с дублирующимися элементами. В результирующем списке не должно быть дубликатов.

def find_duplicates(input_list):
    """Возвращает список дублирующихся элементов без повторений."""
    seen = set()  # Множество для уникальных элементов
    duplicates = set()  # Множество для дубликатов
    
    for item in input_list:
        if item in seen:
            duplicates.add(item)  # Если элемент уже был, добавляем его в дубликаты
        else:
            seen.add(item)  # Иначе добавляем в множество уникальных элементов
    
    return list(duplicates)  # Возвращаем дубликаты как список

# Пример использования
input_list = [1, 2, 3, 4, 1, 2, 5, 6, 7, 2, 3, 8]
result = find_duplicates(input_list)
print("Дублирующиеся элементы:", result)
